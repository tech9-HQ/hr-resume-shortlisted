export default function Sidebar({ currentView, onNavigate, user }) {
  const isActive = (id) =>
    id === "home"
      ? ["home", "new-interview", "interview", "report"].includes(currentView)
      : currentView === id;

  const initials = user?.email
    ? user.email.slice(0, 2).toUpperCase()
    : "??";

  return (
    <aside className="sidebar">
      {/* Top navigation */}
      <nav className="sidebar-nav">
        {TOP_NAV.map((item) => (
          <button
            key={item.id}
            className={`sidebar-item${isActive(item.id) ? " active" : ""}`}
            onClick={() => onNavigate(item.id)}
            title={item.label}
          >
            <span className="sidebar-icon">{item.icon}</span>
            <span className="sidebar-label">{item.label}</span>
          </button>
        ))}
      </nav>

      {/* Bottom: Settings, Help, then profile */}
      <div className="sidebar-bottom">
        {BOTTOM_NAV.map((item) => (
          <button
            key={item.id}
            className={`sidebar-item${isActive(item.id) ? " active" : ""}`}
            onClick={() => onNavigate(item.id)}
            title={item.label}
          >
            <span className="sidebar-icon">{item.icon}</span>
            <span className="sidebar-label">{item.label}</span>
          </button>
        ))}

        <div className="sidebar-divider" />

        <div className="sidebar-profile">
          <div className="sidebar-avatar">{initials}</div>
          <div className="sidebar-user-info">
            <div className="sidebar-user-email" title={user?.email}>{user?.email}</div>
            <button className="sidebar-logout" onClick={() => supabase.auth.signOut()}>
              Sign out
            </button>
          </div>
        </div>
      </div>
    </aside>
  );
}
